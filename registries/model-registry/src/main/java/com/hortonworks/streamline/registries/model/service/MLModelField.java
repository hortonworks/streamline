package com.hortonworks.streamline.registries.model.service;

/**
 * Wrapper class for returning the field name and type information for
 * input and output fields for the pmml model.
 */
public class MLModelField {
    public String type;
    public String name;

    public MLModelField(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public String getName() {
        return name;
    }
}
